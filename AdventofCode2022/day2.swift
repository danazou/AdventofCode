import Foundation

func day2(){
    let input = day2PuzzleInput.split(separator: "\n")

    var strategyGuide: [[String]] = []
    
    for i in input {
        let a = i.split(separator: " ")
//        var game: [String] = []
//        game.append(String(a[0]))
//        game.append(String(a[1]))
//        strategyGuide.append(game)
        strategyGuide.append([String(a[0]), String(a[1])])
    }
    
    // PART I
    let strategyEncryption = ["A":0 ,"X": 0, "B": 1, "Y":1, "C": 2, "Z": 2]
    let outcomeGuide = [-1: "Lose", 2: "Lose", -2: "Win", 1: "Win", 0: "Draw" ]
    
    let shapeScore = ["X": 1, "Y": 2, "Z": 3]
    let roundScore = ["Lose": 0, "Draw": 3, "Win": 6]

    var totalScoreOfPart1 = 0
    for round in strategyGuide{
        let opponent = round[0]
        let me = round[1]
        
        let outcome = strategyEncryption[me]! - strategyEncryption[opponent]!
        let outcomeScore = outcomeGuide[outcome]!
        
        let gameScore = shapeScore[me]! + roundScore[outcomeScore]!
        totalScoreOfPart1 += gameScore

    }

    print ("total score: \(totalScoreOfPart1)")
    
    // PART II
    
    var totalScoreOfPart2 = 0
    let options = ["rock", "paper", "scissor"]
    let opponentEncryptionKey = ["A": "rock" ,"B": "paper", "C": "scissor"]
    let outcomeEncryptionKey = ["X": -1, "Y": 0,  "Z": +1]
    let shapeScorePartTwo = ["rock": 1, "paper": 2, "scissor": 3]
    let roundScorePartTwo = ["X": 0, "Y": 3, "Z": 6]
    
    for round in strategyGuide{
        let opponent = round[0]
        let outcome = outcomeEncryptionKey[round[1]]! // Lose, Draw or Win
        
        let opponentIndex = options.firstIndex(of: opponentEncryptionKey[opponent]!)
        var indexOfMyChoice = opponentIndex! + Int(outcome)
        
        if indexOfMyChoice == -1 {
            indexOfMyChoice = 2
        } else if indexOfMyChoice == 3 {
            indexOfMyChoice = 0
        }

        let myChoice = options[indexOfMyChoice]
        let gameScore = shapeScorePartTwo[myChoice]! + roundScorePartTwo[round[1]]!
        totalScoreOfPart2 += gameScore
    }
    
    print ("total score for part 2: \(totalScoreOfPart2)")

}



